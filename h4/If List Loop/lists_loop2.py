fruit = ["Appel", "Banaan", "Peer", "Kiwi"]
print()

# While loop
print("While loop:")
i = 0
while (i < 3):
    print(fruit[i])
    i = i + 1
    
print()

# For Each loop
print("For Each loop:")
for stukjeFruit in fruit:
    print(stukjeFruit)

print()

